@testset "to_unsigned" begin

    @test BTCParser.to_unsigned((0x01, 0x02)) == reinterpret(UInt16, UInt8[0x01, 0x02])[1]

    @test BTCParser.to_unsigned((0x01, 0x02, 0x03, 0x04)) ==
        reinterpret(UInt32, UInt8[0x01, 0x02, 0x03, 0x04])[1]

    @test BTCParser.to_unsigned((0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08)) ==
        reinterpret(UInt64, UInt8[0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08])[1]

    @test BTCParser.to_unsigned((
        0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08,
        0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08
    )) == reinterpret(UInt128, UInt8[
        0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08,
        0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08
    ])[1]

    @test BTCParser.to_unsigned((
        0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08,
        0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08,
        0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08,
        0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08,
    )) == reinterpret(BTCParser.UInt256, UInt8[
        0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08,
        0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08,
        0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08,
        0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08,
    ])[1]
end

@testset "to_byte_tuple" begin

    @test (0x01, 0x02) == BTCParser.to_byte_tuple(reinterpret(UInt16, UInt8[0x01, 0x02])[1])

    @test (0x01, 0x02, 0x03, 0x04) ==
        BTCParser.to_byte_tuple(reinterpret(UInt32, UInt8[0x01, 0x02, 0x03, 0x04])[1])

    @test (0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08) ==
        BTCParser.to_byte_tuple(reinterpret(UInt64, UInt8[0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08])[1])

    @test (
        0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08,
        0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08
    ) == BTCParser.to_byte_tuple(reinterpret(UInt128, UInt8[
        0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08,
        0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08
    ])[1])

    @test (
        0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08,
        0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08,
        0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08,
        0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08,
    ) == BTCParser.to_byte_tuple(reinterpret(UInt256, UInt8[
        0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08,
        0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08,
        0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08,
        0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08,
    ])[1])
end
